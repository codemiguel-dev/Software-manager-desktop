    public void validar_inicio() {
        
        String correo = textfieldcorreo.getText();
        String clave = String.valueOf(textfieldclave.getPassword());
        
        if (!"".equals(correo) || !"".equals(clave)) {
            clase_login_dueno = loginduenodao.logi(correo, clave);
            if (clase_login_dueno.getCorreo() != null && clase_login_dueno.getClave() != null) {
                LimpiarCamposLogin();
                Admindueno ad = new Admindueno(clase_login_dueno);
                ad.setVisible(true);
                //dispose(); /*cierra el programa*/

            } else {
                JOptionPane.showMessageDialog(null, "correo o contrase침as invalidas");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Los campos no tienen datos");
        }
        
    }
